package 接口;

public class Forg extends Animal implements Swim{

    public Forg() {
    }

    public Forg(String name, int age) {
        super(name, age);
    }

    @Override
    public void eat() {
        System.out.println("青蛙在吃虫子");
    }

    @Override
    public void swim() {
        System.out.println("青蛙在蛙泳");
    }
}
